public class PartA {
    public static void main(String[] args) {
        for(int i = 0; i <= 30; i++){
            System.out.println(i);
        }
        for(int i = 30; i >= 0; i--){
            System.out.println(i);
        }
        for(int i = 0; i<=18; i = i+3){
            System.out.println(i);
        }
        for(int i = 10; i>=0; i = i -2){
            System.out.println(i);
        }
    }
}
